// функцію, яка повертає кількість складів у простому рядку.
// Рядок складається з коротких повторюваних слів типу
// "Lalalalalalala"(які мали б 7 складів ).

function countSyllables(str) {
	return str.match(/[aeuio]/gi).length;
}

